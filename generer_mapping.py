fr_set = set(fr_exact)
ar_set = set(ar_exact)
valid = []
errors = []
used_ar = set()
used_fr = set()

for fi, ai in pairs_idx:
    fn = fr_exact[fi]
    an = ar_exact[ai]
    if fn not in fr_set:
        errors.append(f'FR[{fi}] introuvable: {fn}')
    elif an not in ar_set:
        errors.append(f'AR[{ai}] introuvable: {an}')
    elif an in used_ar:
        errors.append(f'AR[{ai}] deja utilise: {an}')
    elif fn in used_fr:
        errors.append(f'FR[{fi}] deja utilise: {fn}')
    else:
        valid.append({'fr': fn, 'ar': an})
        used_ar.add(an)
        used_fr.add(fn)

print(f'Paires valides: {len(valid)}')
if errors:
    print(f'Erreurs ({len(errors)}):')
    for e in errors: print(' ', e)

(BASE / 'mapping.json').write_text(
    json.dumps(valid, ensure_ascii=False, indent=2), encoding='utf-8')
